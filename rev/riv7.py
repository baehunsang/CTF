def __ROL__(num, count, bits=8): return ((num << count) | (num >> (bits - count))) & ((0b1<<bits) - 1)
# Referance : https://onsoim.tistory.com/entry/RORROL

flag = ''
for i in range(0, 31):
    for a in range(0x00, 0xff):
        if(i ^ __ROL__(a, i & 7) == arr[i]):
            flag += chr(a)
            break
